/**
 * formatDigest.js
 *
 * Prepares the batch input for the premium digest synthesis call.
 * Also formats the final digest output into a clean email-ready HTML/text string.
 *
 * This module does NOT call the AI — it prepares input and formats output.
 * The actual AI call happens in the n8n workflow.
 */

/**
 * Build the batch input payload for the premium digest model.
 * Summarizes all high-value signals into a compact list for a single AI call.
 * Never sends full article bodies — only title, source, lane, signal_type, and summary.
 *
 * @param {Object[]} signals - high-value signals from Google Sheets (score >= 4)
 * @returns {string} formatted batch input string for the prompt
 */
function buildDigestInput(signals) {
  if (!Array.isArray(signals) || signals.length === 0) {
    return 'No high-value signals collected today.';
  }

  const lines = signals.map((s, i) => {
    const num = i + 1;
    const lane = s.lane || 'Unknown';
    const source = s.source || 'Unknown';
    const signalType = s.signal_type || 'Unknown';
    const cluster = Array.isArray(s.keyword_cluster)
      ? s.keyword_cluster.join(', ')
      : s.keyword_cluster || '';
    const title = s.title || '(no title)';
    const url = s.url || '';
    const summary = s.summary || s.ai_excerpt || '(no summary)';

    return [
      `[${num}] LANE: ${lane} | SOURCE: ${source} | TYPE: ${signalType} | CLUSTER: ${cluster}`,
      `TITLE: ${title}`,
      `URL: ${url}`,
      `SUMMARY: ${summary.slice(0, 300)}`,
      '',
    ].join('\n');
  });

  return lines.join('\n');
}

/**
 * Format the AI digest response into a clean HTML email body.
 * Wraps the digest content in simple, readable HTML.
 *
 * @param {string} digestText - raw text output from the premium digest model
 * @param {string} dateStr - today's date string (e.g. "March 11, 2026")
 * @param {number} signalCount - number of high-value signals in today's batch
 * @returns {string} HTML email body
 */
function formatDigestEmail(digestText, dateStr, signalCount) {
  return `
<!DOCTYPE html>
<html>
<head>
  <meta charset="UTF-8">
  <style>
    body { font-family: Georgia, serif; max-width: 680px; margin: 0 auto; padding: 24px; color: #1a1a1a; }
    h1 { font-size: 22px; border-bottom: 2px solid #333; padding-bottom: 8px; }
    h2 { font-size: 16px; color: #444; margin-top: 28px; }
    p { line-height: 1.6; }
    .meta { font-size: 13px; color: #888; margin-bottom: 24px; }
    .section { margin-bottom: 24px; }
    .footer { font-size: 12px; color: #aaa; border-top: 1px solid #eee; padding-top: 12px; margin-top: 32px; }
  </style>
</head>
<body>
  <h1>BizDeedz Signal Engine — Daily Digest</h1>
  <p class="meta">Date: ${dateStr} &nbsp;|&nbsp; High-Value Signals Today: ${signalCount}</p>
  <div class="section">
    ${convertMarkdownToHtml(digestText)}
  </div>
  <div class="footer">
    Generated by BizDeedz Signal Engine &nbsp;|&nbsp; Powered by n8n + AI
  </div>
</body>
</html>
`.trim();
}

/**
 * Minimal markdown-to-HTML converter for digest sections.
 * Handles headers (##, ###), bold (**text**), and line breaks.
 * Does not require a full markdown library.
 *
 * @param {string} text
 * @returns {string} HTML string
 */
function convertMarkdownToHtml(text) {
  if (!text) return '';
  return text
    .replace(/^### (.+)$/gm, '<h3>$1</h3>')
    .replace(/^## (.+)$/gm, '<h2>$1</h2>')
    .replace(/^# (.+)$/gm, '<h2>$1</h2>')
    .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
    .replace(/\*(.+?)\*/g, '<em>$1</em>')
    .replace(/\n{2,}/g, '</p><p>')
    .replace(/\n/g, '<br>')
    .replace(/^/, '<p>')
    .replace(/$/, '</p>');
}

/**
 * Format a plain-text version of the digest for email clients that prefer text.
 * @param {string} digestText
 * @param {string} dateStr
 * @param {number} signalCount
 * @returns {string}
 */
function formatDigestPlainText(digestText, dateStr, signalCount) {
  return [
    'BizDeedz Signal Engine — Daily Digest',
    `Date: ${dateStr} | High-Value Signals Today: ${signalCount}`,
    '='.repeat(60),
    '',
    digestText,
    '',
    '='.repeat(60),
    'Generated by BizDeedz Signal Engine',
  ].join('\n');
}

module.exports = {
  buildDigestInput,
  formatDigestEmail,
  formatDigestPlainText,
  convertMarkdownToHtml,
};
