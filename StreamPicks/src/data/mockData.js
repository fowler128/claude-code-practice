export const getRecentlyAdded = (limit = 10) => {
  return getFilteredContent()
    .sort((a, b) => b.year - a.year)
    .slice(0, limit);
};

// Search content
export const searchContent = (query) => {
  const lowerQuery = query.toLowerCase();
  return getFilteredContent().filter(item =>
    item.title.toLowerCase().includes(lowerQuery) ||
    item.genres.some(g => g.toLowerCase().includes(lowerQuery)) ||
    item.cast.some(c => c.toLowerCase().includes(lowerQuery))
  );
};
