// Minimum IMDB rating threshold
export const MIN_IMDB_RATING = 5.0;

// Filter content by minimum IMDB rating
export const getFilteredContent = () => {
  return MOCK_CONTENT.filter(item => item.imdbRating >= MIN_IMDB_RATING);
};

// Get content by streaming service
export const getContentByService = (serviceId) => {
  return getFilteredContent().filter(item => item.streamingService === serviceId);
};

// Get content by type (movie or tv show)
export const getContentByType = (type) => {
  return getFilteredContent().filter(item => item.type === type);
};

// Get content by genre
export const getContentByGenre = (genre) => {
  return getFilteredContent().filter(item => item.genres.includes(genre));
};

// Get top rated content
export const getTopRated = (limit = 10) => {
  return getFilteredContent()
    .sort((a, b) => b.imdbRating - a.imdbRating)
    .slice(0, limit);
};

