/**
 * Types for the AI Readiness Accelerator application
 */

/**
 * Represents a single response to an assessment question
 */
export interface QuestionResponse {
  questionId: string;
  value: number; // 1-5 scale
}

/**
 * Map of pillar IDs to their question responses
 */
export interface AssessmentResponses {
  [pillarId: string]: QuestionResponse[];
}

/**
 * Definition of an assessment pillar
 */
export interface PillarDefinition {
  id: string;
  name: string;
  description?: string;
  questions?: Question[];
}

/**
 * Assessment question definition
 */
export interface Question {
  id: string;
  text: string;
  category?: string;
}

/**
 * Status levels for individual pillars
 */
export type PillarStatus = 'critical' | 'needs-work' | 'adequate' | 'strong' | 'excellent';

/**
 * Overall readiness levels
 */
export type ReadinessLevel = 'not-ready' | 'foundational' | 'developing' | 'ready' | 'optimized';

/**
 * Individual pillar score result
 */
export interface PillarScore {
  id: string;
  name: string;
  score: number;
  status: PillarStatus;
}

/**
 * Complete scoring results for all pillars
 */
export interface PillarScores {
  pillars: PillarScore[];
  overall: number;
  readinessLevel: ReadinessLevel;
}

/**
 * Strength or gap item with description
 */
export interface StrengthGapItem {
  pillarId: string;
  pillarName: string;
  score: number;
  description: string;
}

/**
 * Identified strengths and gaps from assessment
 */
export interface StrengthsGaps {
  strengths: StrengthGapItem[];
  gaps: StrengthGapItem[];
}

/**
 * Law firm profile information
 */
export interface FirmProfile {
  /** Name of the law firm */
  name: string;
  /** Practice areas the firm specializes in */
  practiceAreas: string[];
  /** Size category of the firm */
  size: 'Solo' | 'Small' | 'Mid-size' | 'Large' | 'Enterprise';
  /** Number of attorneys (optional) */
  attorneyCount?: number;
  /** Current technology tools in use */
  currentTools: string[];
  /** Primary goals for AI adoption (optional) */
  aiGoals?: string[];
}

/**
 * Executive summary generated by AI analysis
 */
export interface ExecutiveSummary {
  /** 2-3 sentence executive summary of the assessment */
  overallAssessment: string;
  /** 3-4 bullet points of main observations */
  keyFindings: string[];
  /** 3 specific next steps ordered by importance */
  priorityActions: string[];
  /** Single most important action for the next 30 days */
  recommendedFirstMove: string;
  /** 2-3 risks to be aware of */
  riskConsiderations: string[];
}

/**
 * API response wrapper for Gemini
 */
export interface GeminiResponse {
  candidates: Array<{
    content: {
      parts: Array<{
        text: string;
      }>;
    };
    finishReason: string;
  }>;
}

/**
 * Error response from Gemini API
 */
export interface GeminiError {
  error: {
    code: number;
    message: string;
    status: string;
  };
}
